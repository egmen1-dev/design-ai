function toPlanningContext(context: PhotographyDirectorAgentContext): PhotographyPlanningContext {
  return {
    randomCameraParams: context.randomCameraParams,
    perspectiveCompositionConflict: context.perspectiveCompositionConflict,
    artificialPhotography: context.artificialPhotography,
    missingLightingStrategy: context.missingLightingStrategy,
    heroLosesAdvantage: context.heroLosesAdvantage,
  };
}

function resolveRetryBranch(context: PhotographyDirectorAgentContext): PhotographyDirectorAgentRetryBranch | undefined {
  if (context.skipRetry) return undefined;
  if (
    context.randomCameraParams ||
    context.perspectiveCompositionConflict ||
    context.artificialPhotography ||
    context.heroLosesAdvantage ||
    context.catalogShootingType ||
    context.lowConfidence
  ) {
    return "style_perspective_realism";
  }
  return undefined;
}

export async function executePhotographyDirectorAgent(input: {
  agentInput?: PhotographyDirectorAgentInput;
  context?: PhotographyDirectorAgentContext;
}): Promise<PhotographyDirectorAgentExecutionReport> {
  const started = Date.now();
  const context = input.context ?? {};
  const maxRetries = context.maxRetries ?? 1;
  const agentInput = input.agentInput ?? buildBatterySprayerPhotographyDirectorInput();
  const violations: PhotographyDirectorAgentViolation[] = [];
  const modulesCompleted: PhotographyDirectorAgentModuleId[] = [];
  const moduleRecords: PhotographyDirectorAgentModuleRecord[] = [];
  let retryCount = 0;
  let retryBranch: PhotographyDirectorAgentRetryBranch | undefined;

  const planningInput = toPhotographyPlanningInput(agentInput);
  const planningContext = toPlanningContext(context);

  let planningReport = runPhotographyPlanningStage(planningInput, planningContext);

  const recordPlanningModules = (section?: PhotographyPlanningSection, suffix = "") => {
    const planned = section?.plannedBlueprint;
    const style = section?.photographyStyle ?? selectPhotographyStyle(planningInput);
    recordModule(moduleRecords, modulesCompleted, PhotographyDirectorAgentModule.PHOTOGRAPHY_STYLE_SELECTOR, `${formatPhotoStyleLabel(style)}${suffix}`);
    recordModule(moduleRecords, modulesCompleted, PhotographyDirectorAgentModule.COMMERCIAL_SHOOTING_ENGINE, `${section?.cameraPreset ?? ""}${suffix}`);
    recordModule(moduleRecords, modulesCompleted, PhotographyDirectorAgentModule.PERSPECTIVE_PLANNER, `${planned?.cameraAngle ?? ""}${suffix}`);
    recordModule(moduleRecords, modulesCompleted, PhotographyDirectorAgentModule.DEPTH_OF_FIELD_PLANNER, `${planned?.depthOfField ?? ""}${suffix}`);
    recordModule(moduleRecords, modulesCompleted, PhotographyDirectorAgentModule.REALISM_CONTROLLER, `${planned?.reflectionStyle ?? ""}${suffix}`);
    recordModule(moduleRecords, modulesCompleted, PhotographyDirectorAgentModule.PHOTOGRAPHY_VALIDATOR, `${planningReport.violations.length} checks${suffix}`);
    recordModule(moduleRecords, modulesCompleted, PhotographyDirectorAgentModule.PHOTOGRAPHY_BLUEPRINT_BUILDER, "blueprint assembled" + suffix);
  };

  recordPlanningModules(planningReport.section);

  let blueprint = planningReport.section
    ? fromPlannedPhotographyBlueprint(
        planningReport.section.plannedBlueprint,
        planningReport.section.photographyStyle,
        agentInput,
        planningReport.section.confidence,
      )
    : undefined;

  let confidence = blueprint?.confidence ?? 0;
  if (context.lowConfidence) confidence = 0.55;

  if (context.catalogShootingType && blueprint) {
    blueprint = { ...blueprint, shootingType: "catalog_photography" };
  }

  for (const v of planningReport.violations) {
    violations.push(violation(v.code as PhotographyDirectorAgentFailureCode, v.message));
  }
  violations.push(
    ...validatePhotographyDirectorAgentBlueprint(
      blueprint,
      agentInput,
      planningReport.section?.plannedBlueprint,
    ),
  );

  while (retryCount < maxRetries && !context.skipRetry) {
    const branch = resolveRetryBranch(context);
    if (!branch || confidence >= CONFIDENCE_THRESHOLD) break;

    retryCount += 1;
    retryBranch = branch;

    const retryContext: PhotographyPlanningContext = {
      randomCameraParams: false,
      perspectiveCompositionConflict: false,
      artificialPhotography: false,
      missingLightingStrategy: false,
      heroLosesAdvantage: false,
    };

    planningReport = runPhotographyPlanningStage(planningInput, retryContext);
    blueprint = planningReport.section
      ? fromPlannedPhotographyBlueprint(
          planningReport.section.plannedBlueprint,
          planningReport.section.photographyStyle,
          agentInput,
          planningReport.section.confidence,
        )
      : blueprint;
    confidence = blueprint?.confidence ?? confidence;

    violations.length = 0;
    for (const v of planningReport.violations) {
      violations.push(violation(v.code as PhotographyDirectorAgentFailureCode, v.message));
    }
    violations.push(
      ...validatePhotographyDirectorAgentBlueprint(
        blueprint,
        agentInput,
        planningReport.section?.plannedBlueprint,
      ),
    );
    recordPlanningModules(planningReport.section, ` retry ${retryCount}`);
  }

  if (retryCount > 0 && planningReport.valid) {
    confidence = Math.max(confidence, CONFIDENCE_THRESHOLD);
  }

  if (context.artificialPhotography && retryCount >= maxRetries && !context.skipRetry && !planningReport.valid) {
    violations.push(violation("RETRY_EXHAUSTED", "Style selector and realism retry did not resolve artificial look"));
  }

  const bp = createEmptyRenderBlueprint({
    category: agentInput.productProfile.category,
    seed: 37,
  });
  const workingContext = buildAgentContextPackage({
    blueprint: bp,
    agentId: PHOTOGRAPHY_DIRECTOR_AGENT_ID,
  });
  const memoryPackage = buildAgentMemoryPackage({
    agentId: PHOTOGRAPHY_DIRECTOR_AGENT_ID,
    working: workingContext,
  });
  releaseAgentMemory(memoryPackage);

  const decision = await executeProfessionalDecision({
    agentId: PHOTOGRAPHY_DIRECTOR_AGENT_ID as AgentContractId,
    blueprint: bp,
  });
  if (!decision.valid) {
    violations.push(violation("EXECUTION_FAILED", "Professional decision must validate photography direction"));
  }
  if (!decision.state.problem?.professionalQuestion.toLowerCase().includes("professional")) {
    violations.push(violation("EXECUTION_FAILED", "Decision problem must be photography-focused"));
  }

  const marketplaceAligned =
    planningInput.marketplace === (agentInput.productProfile.marketplace ?? "wildberries");
  const durationMs = Date.now() - started;

  const kpis = buildPhotographyDirectorAgentKpis({
    blueprint: blueprint ?? {
      photoStyle: "",
      shootingType: "",
      framing: "",
      perspective: "",
      depthOfField: "",
      realismLevel: "",
      commercialMood: "",
      lensProfile: "",
      confidence: 0,
    },
    planned: planningReport.section?.plannedBlueprint ?? {
      cameraPreset: "",
      lens: "",
      cameraAngle: "",
      cameraHeight: "",
      depthOfField: "",
      lightingPreset: "",
      exposure: "",
      whiteBalance: "",
      shadowStyle: "",
      reflectionStyle: "",
    },
    confidence,
    retryCount,
    planningValid: planningReport.valid,
    marketplaceAligned,
  });

  const uniqueViolations = dedupeViolations(violations);
  const modulesComplete =
    modulesCompleted.length >= PHOTOGRAPHY_DIRECTOR_AGENT_MODULES.length ||
    PHOTOGRAPHY_DIRECTOR_AGENT_MODULES.every((m) => modulesCompleted.includes(m.id));

  return {
    valid: uniqueViolations.length === 0 && planningReport.valid && modulesComplete && Boolean(blueprint),
    agentId: PHOTOGRAPHY_DIRECTOR_AGENT_ID,
    violations: uniqueViolations,
    modulesCompleted,
    moduleRecords,
    input: agentInput,
    blueprint,
    planningSection: planningReport.section,
    confidence,
    retryCount,
    retryBranch,
    durationMs,
    kpis,
    pipelineMediated: true,
    lightingExcluded: true,
    goldenRuleSatisfied: PHOTOGRAPHY_DIRECTOR_AGENT_GOLDEN_RULE.includes("believe the product"),
  };
}

export async function executePhotographyDirectorAgentWithPipeline(input: {
  agentInput?: PhotographyDirectorAgentInput;
  context?: PhotographyDirectorAgentContext;
}): Promise<PhotographyDirectorAgentExecutionReport> {
  const report = await executePhotographyDirectorAgent(input);
  if (!report.valid || !report.blueprint) return report;

  const pipelineValid =
    PHOTOGRAPHY_DIRECTOR_AGENT_PIPELINE.length === 2 &&
    PHOTOGRAPHY_DIRECTOR_AGENT_PIPELINE[0].to === "photography_director" &&
    PHOTOGRAPHY_DIRECTOR_AGENT_PIPELINE[1].to === "lighting_director";

  if (!pipelineValid) {
    report.violations.push(violation("DIRECT_AGENT_HANDOFF", "Pipeline position chain is invalid"));
    report.valid = false;
  }

  if (report.agentId !== COMMERCIAL_PHOTO_DIRECTOR_ID) {
    report.violations.push(violation("EXECUTION_FAILED", "Agent must use commercial-photo-director contract"));
    report.valid = false;
  }

  return report;
}

function dedupeViolations(violations: PhotographyDirectorAgentViolation[]): PhotographyDirectorAgentViolation[] {
  const seen = new Set<string>();
  return violations.filter((v) => {
    const key = `${v.code}:${v.module ?? ""}:${v.message}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

export function validatePhotographyDirectorAgentStructure(): PhotographyDirectorAgentViolation[] {
  if (PHOTOGRAPHY_DIRECTOR_AGENT_MODULES.length !== 7) {
    return [violation("MODULE_INCOMPLETE", "Photography Director Agent requires 7 internal modules")];
  }
  return [];
}

export function validatePhotographyDirectorAgent(
  context: PhotographyDirectorAgentContext = {},
): PhotographyDirectorAgentValidationReport {
  const violations = [...validatePhotographyDirectorAgentStructure()];
  return {
    valid: violations.length === 0,
    violations,
    modulesComplete: validatePhotographyDirectorAgentStructure().length === 0,
    pipelinePositionValid: PHOTOGRAPHY_DIRECTOR_AGENT_PIPELINE[1].to === "lighting_director",
    kitchenExecutionValid: false,
    successCriteriaMet: violations.length === 0,
  };
}

export async function validatePhotographyDirectorAgentWithExecution(
  context: PhotographyDirectorAgentContext = {},
): Promise<PhotographyDirectorAgentValidationReport> {
  const report = validatePhotographyDirectorAgent(context);
  const kitchen = await executePhotographyDirectorAgent({
    agentInput: buildBatterySprayerPhotographyDirectorInput(),
    context,
  });
  const violations = dedupeViolations([...report.violations, ...kitchen.violations]);
  return {
    ...report,
    valid: violations.length === 0 && kitchen.valid,
    violations,
    kitchenExecutionValid: kitchen.valid,
    successCriteriaMet: violations.length === 0 && kitchen.valid,
  };
}

export function assertPhotographyDirectorAgent(
  context?: PhotographyDirectorAgentContext,
): PhotographyDirectorAgentValidationReport {
  const report = validatePhotographyDirectorAgent(context);
  if (!report.valid) {
    throw new Error(`Photography Director Agent violated: ${report.violations.map((v) => v.message).join("; ")}`);
  }
  return report;
}

export async function runPhotographyDirectorAgent(
  context: PhotographyDirectorAgentContext = {},
): Promise<PhotographyDirectorAgentValidationReport> {
  return validatePhotographyDirectorAgentWithExecution(context);
}

export function isPhotographyDirectorAgentFailure(code: string): code is PhotographyDirectorAgentFailureCode {
  const codes: PhotographyDirectorAgentFailureCode[] = [
    "MODULE_INCOMPLETE",
    "RANDOM_CAMERA_PARAMS",
    "PERSPECTIVE_COMPOSITION_CONFLICT",
    "ARTIFICIAL_PHOTOGRAPHY",
    "MISSING_LIGHTING_STRATEGY",
    "HERO_LOSES_ADVANTAGE",
    "CATALOG_SHOOTING_MISMATCH",
    "STORY_STYLE_MISMATCH",
    "BLUEPRINT_INCOMPLETE",
    "LOW_CONFIDENCE",
    "LOW_REALISM",
    "RETRY_EXHAUSTED",
    "DESIGN_DECISION_DETECTED",
    "CONTAINS_COMPOSITION_DECISION",
    "DIRECT_AGENT_HANDOFF",
    "EXECUTION_FAILED",
  ];
  return codes.includes(code as PhotographyDirectorAgentFailureCode);
}

export function getPhotographyDirectorAgentModule(
  moduleId: PhotographyDirectorAgentModuleId,
): PhotographyDirectorAgentModuleDefinition | undefined {
  return PHOTOGRAPHY_DIRECTOR_AGENT_MODULES.find((m) => m.id === moduleId);
}

export function scorePhotographyCandidateForStory(
  candidate: string,
  storyPattern: string,
): number {
  if (candidate.includes("Outdoor") && storyPattern.includes("Problem")) return 0.96;
  if (candidate.includes("Lifestyle") && storyPattern.includes("Problem")) return 0.94;
  if (candidate.includes("Premium") && storyPattern.includes("Premium")) return 0.93;
  return 0.82;
}

export function validatePhotographySupportsStory(
  shootingType: string,
  storyPrimaryMessage: string,
): boolean {
  if (storyPrimaryMessage.toLowerCase().includes("tree") || storyPrimaryMessage.toLowerCase().includes("garden")) {
    return !/catalog/i.test(shootingType);
  }
  return shootingType.length > 0;
}

export function isInUseWorkflowShooting(shootingType: string): boolean {
  return shootingType === "in_use_workflow" || shootingType === "commercial_lifestyle";
}
