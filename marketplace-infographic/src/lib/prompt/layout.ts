export function buildLayoutPrompt(referenceColors?: string[]): string {
  const colors = referenceColors?.join(", ") ?? "#00a8b5, #ffffff, #0f172a";

  return `КОМПОЗИЦИЯ (layout: marketplace, холст 900×1200)
- Заголовок слева вверху, Title Case (НЕ КАПС), 40–65% ширины кадра
- Pill-подзаголовок с иконкой молнии, сразу под заголовком
- Слева: одна hero-цифра (об/мин или главный параметр) + подарок внизу плашки
- Справа: компактная колонка — АКБ и насадки (без дублирования левых цифр)
- Товар по диагонали, 68–88% высоты кадра, центр смещён 52–56% по X
- Палитра референса: [${colors}]
- objectScale: 0.72–0.82 для крупного товара

bullets — РОВНО 5 уникальных строк:
  [0] hero — об/мин или главный тех. параметр
  [1] подарок / комплектация
  [2] кол-во АКБ
  [3] кол-во насадок
  [4] технологии / качество бренда`;
}

export function buildCompositionRules(): string {
  return `ПРАВИЛА КОМПОЗИЦИИ
- Rule of Thirds: товар на пересечении линий
- Golden Ratio: заголовок в верхней трети
- Negative Space: не перегружать текстом
- Visual Flow: взгляд идёт заголовок → товар → УТП
- Z-pattern для чтения слева направо
- Иерархия: headline > subHeadline > benefits > детали`;
}
