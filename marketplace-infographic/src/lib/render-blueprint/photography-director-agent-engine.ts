function recordModule(
  records: PhotographyDirectorAgentModuleRecord[],
  completed: PhotographyDirectorAgentModuleId[],
  module: PhotographyDirectorAgentModuleId,
  detail?: string,
): void {
  completed.push(module);
  records.push({ module, at: Date.now(), detail });
}

function isGardenProduct(profile: PhotographyDirectorAgentInput["productProfile"]): boolean {
  const sub = profile.subcategory.toLowerCase();
  const cat = profile.category.toLowerCase();
  return sub.includes("sprayer") || cat.includes("garden");
}

export function formatPhotoStyleLabel(style: string): string {
  return PHOTO_STYLE_LABELS[style] ?? style.replace(/_/g, " ");
}

export function formatPerspectiveLabel(angle: string): string {
  return PERSPECTIVE_LABELS[angle] ?? angle.replace(/_/g, " ");
}

export function formatDepthOfFieldLabel(depth: string): string {
  return DEPTH_LABELS[depth] ?? depth.replace(/_/g, " ");
}

export function formatLensProfile(lens: string): string {
  return LENS_LABELS[lens] ?? lens;
}

export function deriveShootingType(
  planned: PlannedPhotographyBlueprint,
  input: PhotographyDirectorAgentInput,
): string {
  if (planned.cameraPreset === CameraPreset.TECHNICAL_DOCUMENTATION) {
    return "technical_documentation";
  }
  const scene = toPlannedSceneFromAgentBlueprint(input.sceneBlueprint);
  if (scene.sceneType === SceneCategory.OUTDOOR || isGardenProduct(input.productProfile)) {
    return "in_use_workflow";
  }
  if (planned.cameraPreset === CameraPreset.STUDIO_PRODUCT) {
    return "studio_product";
  }
  return "commercial_lifestyle";
}

export function deriveFraming(
  planned: PlannedPhotographyBlueprint,
  input: PhotographyDirectorAgentInput,
): string {
  const heroWidth = input.layoutBlueprint.heroPlacement.width;
  const heightLabel = planned.cameraHeight === "high" ? "elevated" : "eye_level";
  return `${heightLabel} framing, hero ${Math.round(heroWidth * 100)}% width, overlay reserve preserved`;
}

export function deriveRealismLevel(planned: PlannedPhotographyBlueprint): string {
  if (planned.shadowStyle === "harsh_artificial" || planned.lens === "unknown") return "low";
  if (planned.reflectionStyle.includes("impossible")) return "low";
  return "high";
}

export function computeRealismScore(planned: PlannedPhotographyBlueprint): number {
  if (planned.lens === "unknown" || planned.cameraPreset === "random_camera") return 0.3;
  if (planned.shadowStyle === "harsh_artificial") return 0.4;
  if (planned.depthOfField === "extreme_background_blur") return 0.55;
  if (planned.reflectionStyle.includes("soft_matte") || planned.reflectionStyle.includes("natural")) return 0.92;
  return 0.88;
}

export function fromPlannedPhotographyBlueprint(
  planned: PlannedPhotographyBlueprint,
  style: PlannedPhotographyStyleId,
  input: PhotographyDirectorAgentInput,
  confidence: number,
): PhotographyDirectorAgentBlueprint {
  return {
    photoStyle: formatPhotoStyleLabel(style),
    shootingType: deriveShootingType(planned, input),
    framing: deriveFraming(planned, input),
    perspective: formatPerspectiveLabel(planned.cameraAngle),
    depthOfField: formatDepthOfFieldLabel(planned.depthOfField),
    realismLevel: deriveRealismLevel(planned),
    commercialMood: input.storyBlueprint.emotionalDirection,
    lensProfile: formatLensProfile(planned.lens),
    confidence,
  };
}

export function validatePhotographyDirectorAgentBlueprint(
  blueprint?: PhotographyDirectorAgentBlueprint,
  input?: PhotographyDirectorAgentInput,
  planned?: PlannedPhotographyBlueprint,
): PhotographyDirectorAgentViolation[] {
  const violations: PhotographyDirectorAgentViolation[] = [];
  if (!blueprint) {
    violations.push(
      violation("BLUEPRINT_INCOMPLETE", "Photography Blueprint is required", PhotographyDirectorAgentModule.PHOTOGRAPHY_BLUEPRINT_BUILDER),
    );
    return violations;
  }

  if (/catalog/i.test(blueprint.shootingType) && input && isGardenProduct(input.productProfile)) {
    violations.push(
      violation("CATALOG_SHOOTING_MISMATCH", "Garden product must use in-use workflow, not catalog photography", PhotographyDirectorAgentModule.COMMERCIAL_SHOOTING_ENGINE),
    );
  }

  if (blueprint.realismLevel === "low") {
    violations.push(
      violation("ARTIFICIAL_PHOTOGRAPHY", "Photography must look professionally plausible", PhotographyDirectorAgentModule.REALISM_CONTROLLER),
    );
  }

  if (blueprint.perspective === "Top Angle" && input?.layoutBlueprint.layoutPattern.includes("Marketplace")) {
    violations.push(
      violation("PERSPECTIVE_COMPOSITION_CONFLICT", "Top angle conflicts with marketplace hero composition", PhotographyDirectorAgentModule.PERSPECTIVE_PLANNER),
    );
  }

  if (!blueprint.lensProfile || !blueprint.photoStyle) {
    violations.push(
      violation("RANDOM_CAMERA_PARAMS", "Lens and photo style must be story-driven", PhotographyDirectorAgentModule.PERSPECTIVE_PLANNER),
    );
  }

  if (planned && !planned.lightingPreset) {
    violations.push(
      violation("MISSING_LIGHTING_STRATEGY", "Lighting strategy must be defined before handoff", PhotographyDirectorAgentModule.PHOTOGRAPHY_VALIDATOR),
    );
  }

  if (planned?.depthOfField === "extreme_background_blur") {
    violations.push(
      violation("HERO_LOSES_ADVANTAGE", "Depth of field must keep hero product visually dominant", PhotographyDirectorAgentModule.DEPTH_OF_FIELD_PLANNER),
    );
  }

  const serialized = JSON.stringify(blueprint);
  if (COMPOSITION_KEYWORDS.test(serialized)) {
    violations.push(
      violation("CONTAINS_COMPOSITION_DECISION", "Photography must not decide composition layout", PhotographyDirectorAgentModule.PHOTOGRAPHY_VALIDATOR),
    );
  }

  const realism = planned ? computeRealismScore(planned) : blueprint.realismLevel === "high" ? 0.9 : 0.4;
  if (realism < REALISM_MIN_SCORE) {
    violations.push(
      violation("LOW_REALISM", "Realism score below acceptable threshold", PhotographyDirectorAgentModule.REALISM_CONTROLLER),
    );
  }

  return violations;
}

export function buildPhotographyDirectorAgentKpis(input: {
  blueprint: PhotographyDirectorAgentBlueprint;
  planned: PlannedPhotographyBlueprint;
  confidence: number;
  retryCount: number;
  planningValid: boolean;
  marketplaceAligned: boolean;
}): PhotographyDirectorAgentKpi {
  const { blueprint, planned, confidence, retryCount, planningValid, marketplaceAligned } = input;
  const realism = computeRealismScore(planned);
  return {
    realismScore: realism,
    commercialPhotographyScore: planningValid && blueprint.photoStyle ? 0.93 : 0.5,
    productTrustScore: blueprint.realismLevel === "high" ? 0.91 : 0.45,
    perspectiveQuality: blueprint.perspective.includes("Quarter") || blueprint.perspective.includes("Eye") ? 0.92 : 0.7,
    marketplaceFit: marketplaceAligned ? 0.9 : 0.65,
    heroVisibility: planned.depthOfField.includes("product_sharp") || planned.depthOfField.includes("moderate") ? 0.92 : 0.75,
    retryRate: retryCount > 0 ? retryCount / (retryCount + 1) : 0,
    confidenceScore: confidence,
  };
}

