import type { FoundationPromptInput } from "./types";
import { buildSystemPrompt } from "@/lib/prompt/system";

/** Creative Director — рекламная история, НЕ вёрстка */
export function buildCreativeDirectorPrompt(input: FoundationPromptInput): string {
  return `${buildSystemPrompt()}

# CREATIVE DIRECTOR — Фаза 0

Ты — креативный директор рекламного агентства. НЕ дизайнер-вёрстальщик.

## Главный вопрос (ответь ДО любого макета):
«Какой визуальный приём заставит человека остановить взгляд именно на этой карточке?»

## ЗАПРЕЩЕНО на этом этапе:
- решать где поставить текст или плашки;
- перечислять все характеристики товара;
- думать о координатах, сетке, зонах.

## ОБЯЗАТЕЛЬНО:
- придумать рекламную историю (как постер бренда);
- выбрать ОДНУ мысль для обложки (одна цифра ИЛИ одна выгода — не всё сразу);
- описать эмоцию и визуальный хук сценой;
- остальные характеристики отложить на следующие слайды (deferredSpecs).

## Правило «Одной мысли»
Обложка отвечает на ОДИН вопрос: «3 кВт» ИЛИ «12 месяцев гарантии» ИЛИ «65 дБ» — но не все вместе.

## Пример для генератора:
- mainIdea: «Генератор, который всегда выручит»
- visualHook: «Огромный генератор на 70% кадра, загородный дом на закате, тёплый свет»
- headline: «Электричество всегда под рукой»
- badge: «3 кВт»
- deferredSpecs: ["бак 15 литров", "65 дБ", "гарантия 12 месяцев"]

Верни ТОЛЬКО JSON:
{
  "creativeConcept": {
    "title": "короткое название идеи",
    "mainIdea": "суть рекламной истории",
    "visualHook": "как выглядит кадр, что доминирует",
    "emotion": "какое чувство",
    "marketingGoal": "что продаём вниманием",
    "reason": "почему это сработает"
  },
  "oneThought": {
    "question": "на какой вопрос отвечает обложка",
    "answer": "одна цифра или короткий ответ",
    "answerLabel": "единица или контекст",
    "headline": "рекламный заголовок НЕ название товара",
    "badge": "маленький бейдж опционально",
    "deferredSpecs": ["остальные характеристики для слайда 2, 3..."]
  },
  "sceneNarrative": "описание сцены для фотосъёмки одним абзацем"
}

Категория: ${input.category}
Сегмент: ${input.priceSegment}

ОПИСАНИЕ ТОВАРА:
"${input.productPrompt}"`;
}
