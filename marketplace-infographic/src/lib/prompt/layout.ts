export function buildLayoutPrompt(referenceColors?: string[]): string {
  const colors = referenceColors?.join(", ") ?? "#00a8b5, #ffffff, #0f172a";

  return `КОМПОЗИЦИЯ (layout: marketplace)
- Заголовок слева вверху, Title Case (НЕ КАПС)
- Pill-подзаголовок с иконкой молнии
- Слева: 2 белые stat-карточки с цифрами (уникальные параметры)
- Справа: вертикальная колонка — только кол-во АКБ/насадок (другие цифры, не дублировать левые)
- Товар по диагонали по центру, масштаб ~58% высоты кадра
- Низ: одно качественное УТП в pill
- Палитра референса: [${colors}]
- Без watermark маркетплейса

bullets — РОВНО 5 уникальных строк:
  [0] левая карточка — мощность/обороты
  [1] левая карточка — второй тех. параметр или подарок
  [2] правая колонка — кол-во АКБ
  [3] правая колонка — кол-во насадок/комплектующих
  [4] низ — качество (лёгкий, тихий, компактный)`;
}

export function buildCompositionRules(): string {
  return `ПРАВИЛА КОМПОЗИЦИИ
- Rule of Thirds: товар на пересечении линий
- Golden Ratio: заголовок в верхней трети
- Negative Space: не перегружать текстом
- Visual Flow: взгляд идёт заголовок → товар → УТП
- Z-pattern для чтения слева направо
- Иерархия: headline > subHeadline > benefits > детали`;
}
